export function classify(key) {
  if (TRACKING_PARAMS[key]) return 'tracker';
  for (const { re } of TRACKING_PATTERNS) {
    if (re.test(key)) return 'tracker';
  }
  return 'other';
}

export function classifyWithDomain(key, domain) {
  // Сначала проверяем домен-специфичные правила
  for (const [domainPattern, params] of Object.entries(DOMAIN_RULES)) {
    if (domain && domain.includes(domainPattern) && params.includes(key)) {
      return 'tracker';
    }
  }
  return classify(key);
}